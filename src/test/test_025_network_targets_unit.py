def _http_result(status_code: int, error: str | None = None) -> HttpResult:
    """Fake HTTP response with given status code."""
    return HttpResult(
        url="https://sftd.example.com/",
        status_code=status_code,
        body="",
        headers={},
        duration_seconds=0.1,
        success=200 <= status_code < 400,
        error=error,
    )


def test_sftd_reachable_responds() -> None:
    """HTTP 200; SFTd is responsive."""
    target: SftdReachable = SftdReachable(make_minimal_config(), _make_terminal(), _make_logger())

    with patch("src.lib.base_target.http_get", return_value=_http_result(200)):
        result: bool = target.collect()

    assert result is True



# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------

def _make_terminal() -> Terminal:
    """Quiet terminal for tests; no noise in output."""
    return Terminal(verbosity=Verbosity.QUIET, use_color=False)


def _make_logger() -> Logger:
    """Logger that only shows errors; quiet for tests."""
    return Logger(level=LogLevel.ERROR)


def _ssh_cmd_result(stdout: str) -> CommandResult:
    """Successful SSH command result with given output."""
    return CommandResult(
        command="ssh test",
        exit_code=0,
        stdout=stdout,
        stderr="",
        duration_seconds=0.1,
        success=True,
        timed_out=False,
    )


def _kubectl_cmd_result(stdout: str = "{}") -> CommandResult:
    """Successful kubectl command result with given JSON output."""
    return CommandResult(
        command="kubectl get",
        exit_code=0,
        stdout=stdout,
        stderr="",
        duration_seconds=0.1,
        success=True,
        timed_out=False,
    )


# ---------------------------------------------------------------------------
# _count_rules helper
# ---------------------------------------------------------------------------

def test_count_rules_iptables() -> None:
    """Iptables format uses «-A» lines for rules; should count all three."""
    output: str = (
        "# Generated by iptables-save\n"
        "*filter\n"
        ":INPUT ACCEPT [0:0]\n"
        "-A INPUT -p tcp --dport 22 -j ACCEPT\n"
        "-A INPUT -p tcp --dport 443 -j ACCEPT\n"
        "-A FORWARD -j DROP\n"
        "COMMIT\n"
    )
    assert _count_rules("iptables", output) == 3


def test_count_rules_nftables() -> None:
    """Nftables counts «accept» and «drop» keywords; we get 3 total."""
    output: str = (
        "table inet filter {\n"
        "  chain input {\n"
        "    type filter hook input priority 0;\n"
        "    ct state established,related accept\n"
        "    tcp dport 22 accept\n"
        "    drop\n"
        "  }\n"
        "}\n"
    )
    assert _count_rules("nftables", output) == 3


def test_count_rules_ufw() -> None:
    """Ufw format has two actual rules after the header row."""
    output: str = (
        "Status: active\n"
        "To                         Action      From\n"
        "--                         ------      ----\n"
        "22/tcp                     ALLOW       Anywhere\n"
        "443/tcp                    ALLOW       Anywhere\n"
    )
    assert _count_rules("ufw", output) == 2


def test_count_rules_empty() -> None:
    """Empty output means zero rules, obviously."""
    assert _count_rules("iptables", "") == 0


def test_count_rules_unknown_type() -> None:
    """Unknown firewall type; can't parse so returns 0."""
    assert _count_rules("unknown", "some output") == 0



def test_turn_connectivity_all_signals() -> None:
    """UDP works, TCP works, coturn pod is running; everything checks out."""
    pods_data: dict[str, Any] = {"items": [
        {"status": {"phase": "Running"}},
    ]}
    target: TurnConnectivity = TurnConnectivity(make_minimal_config(), _make_terminal(), _make_logger())

    with patch("src.lib.base_target.run_command", side_effect=[
        _ssh_cmd_result("EXIT:0"),
        _ssh_cmd_result("OPEN"),
    ]):
        with patch("src.lib.base_target.kubectl_get", return_value=(_kubectl_cmd_result(), pods_data)):
            result: bool = target.collect()

    assert result is True



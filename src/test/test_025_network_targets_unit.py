def test_port_reachability_description() -> None:
    """Should mention 443 in the description."""
    target: PortReachability = PortReachability(make_minimal_config(), _make_terminal(), _make_logger())
    assert "443" in target.description


def test_port_reachability_all_open() -> None:
    """All three ports open; should report 3/3."""
    target: PortReachability = PortReachability(make_minimal_config(), _make_terminal(), _make_logger())

    with patch("src.lib.ssh.SSHTarget.run", return_value=_ssh_cmd_result("OPEN\n")):
        result: str = target.collect()

    assert "3/3" in result


def test_port_reachability_some_closed() -> None:
    """One port open, two closed; should report 1/3."""
    call_count: int = 0

    def _alternating(*args: object, **kwargs: object) -> CommandResult:
        nonlocal call_count
        call_count += 1
        if call_count == 1:
            return _ssh_cmd_result("OPEN\n")
        return _ssh_cmd_result("CLOSED\n")

    target: PortReachability = PortReachability(make_minimal_config(), _make_terminal(), _make_logger())

    with patch("src.lib.ssh.SSHTarget.run", side_effect=_alternating):
        result: str = target.collect()

    assert "1/3" in result


